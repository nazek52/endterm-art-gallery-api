package com.example.artgallery.dto;

public class ArtworkRequest {

    public String title;
    public double price;
    public String type;

    public ArtworkRequest() {
    }

    public String getTitle() {
        return title;
    }

    public double getPrice() {
        return price;
    }

    public String getType() {
        return type;
    }
}
